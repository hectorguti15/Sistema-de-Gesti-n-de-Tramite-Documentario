    private void completarUsuarios() {
        User[] usuarios = Servicio.UsersServicios.users;

        DefaultTableModel model = new DefaultTableModel(
                new Object[][]{},
                new String[]{"Nombre", "Apellido", "Tipo Identificacion", "Identificacion", "Expedientes"}
        ) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        jTable1.setModel(model);

        for (int i = 0; i < usuarios.length; i++) {
            if (usuarios[i] != null) {

                model.addRow(new Object[]{
                    usuarios[i].getNombre(),
                    usuarios[i].getApellido(),
                    TipoIdentificacion.fromEnumToString(usuarios[i].getTipoIdentificacion()),
                    "".equals(usuarios[i].getNumeroIdentificacion()) ? "No es una persona" : usuarios[i].getNumeroIdentificacion(),
                    usuarios[i].getExpedientes().getLength(),});
            } else {
                System.out.println("Expediente en posiciÃ³n " + i + " es null.");
            }
        }
    }


    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String nombre = nombreField.getText().trim();
            String apellido = apellidoField.getText().trim();
            String email = emailField.getText().trim();
            String nTrabajador = nTrabajadorField.getText().trim();
            TipoDependencia tipoDependencia = TipoDependencia.fromStringToEnum((String) jComboBox1.getSelectedItem());

            if (nombre.isEmpty() || apellido.isEmpty() || email.isEmpty() || nTrabajador.isEmpty()) {
                throw new IllegalArgumentException("Todos los campos son requeridos");
            }
            if (!Utils.Utils.esEmailValido(email)) {
                throw new IllegalArgumentException("Correo no valido");
            }
            PersonaDependencia trabajadorNuevo = new PersonaDependencia(nombre, apellido, email, nTrabajador, tipoDependencia);
            AreaDependencia areaDependencia = null;
            switch (tipoDependencia) {
                case AREA_RECEPCION_DOCUMENTOS:
                    areaDependencia = Servicio.AreasServicios.areaRecepcionDocumentos;
                    break;
                case AREA_TRAMITE_DOCUMENTARIO:
                    areaDependencia = Servicio.AreasServicios.areaTramiteDocumentario;
                    break;
                case AREA_REGISTROS_ACADEMICOS:
                    areaDependencia = Servicio.AreasServicios.areaRegistrosAcademicos;
                    break;
                case SECRETARIA_ACADEMICA:
                    areaDependencia = Servicio.AreasServicios.secretariaAcademica;
                    break;
                case OFICINA_SERVICIO_GENERALES:
                    areaDependencia = Servicio.AreasServicios.oficinaServicioGenerales;
                    break;
                case SECRETARIA_ADMINISTRATIVA:
                    areaDependencia = Servicio.AreasServicios.secretariaAdministrativa;
                    break;
                case OFICINA_RECURSOS_HUMANOS:
                    areaDependencia = Servicio.AreasServicios.oficinaRecursosHumanos;
                    break;
                case UNIDAD_ATENCION_USUARIO:
                    areaDependencia = Servicio.AreasServicios.unidadAtencionUsuario;
                    break;
                case OFICINA_QUEJAS:
                    areaDependencia = Servicio.AreasServicios.oficinaQuejas;
                    break;
                case SECRETARIA_GENERAL:
                    areaDependencia = Servicio.AreasServicios.secretariaGeneral;
                    break;
                case POR_ASIGNAR:
                    JOptionPane.showMessageDialog(this, "El tipo de dependencia estÃ¡ por asignar.", "InformaciÃ³n", JOptionPane.INFORMATION_MESSAGE);

                default:
                    JOptionPane.showMessageDialog(this, "Tipo de dependencia desconocido.", "Error", JOptionPane.ERROR_MESSAGE);

            }
            System.out.println("SERVICIOS PERSODADADADANAL" + areaDependencia.getPersonalDependencia().length); 
            areaDependencia.agregarPersona(trabajadorNuevo);
            System.out.println("SERVICIOS PERSONAL" +areaDependencia.getPersonalDependencia().length);
            Servicio.AreasServicios.configurarAreaPersonal();
            System.out.println("SETEADO DE NUEVOI");
            dispose();

        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

